package com.bean;

public class Shopping {
	private int shopping_id;
	private int shopping_goods;
	private int shopping_price;
	private int shopping_num;
	private int shopping_userid;
	public int getShopping_id() {
		return shopping_id;
	}
	public void setShopping_id(int shopping_id) {
		this.shopping_id = shopping_id;
	}
	public int getShopping_goods() {
		return shopping_goods;
	}
	public void setShopping_goods(int shopping_goods) {
		this.shopping_goods = shopping_goods;
	}
	public int getShopping_price() {
		return shopping_price;
	}
	public void setShopping_price(int shopping_price) {
		this.shopping_price = shopping_price;
	}
	public int getShopping_num() {
		return shopping_num;
	}
	public void setShopping_num(int shopping_num) {
		this.shopping_num = shopping_num;
	}
	public int getShopping_userid() {
		return shopping_userid;
	}
	public void setShopping_userid(int shopping_userid) {
		this.shopping_userid = shopping_userid;
	}
	
}
