package com.bean;

public class Details {
	private int details_id;
	private String details_order;
	private int details_goods;
	private int details_price;
	private int details_num;
	public int getDetails_id() {
		return details_id;
	}
	public void setDetails_id(int details_id) {
		this.details_id = details_id;
	}
	public String getDetails_order() {
		return details_order;
	}
	public void setDetails_order(String details_order) {
		this.details_order = details_order;
	}
	public int getDetails_goods() {
		return details_goods;
	}
	public void setDetails_goods(int details_goods) {
		this.details_goods = details_goods;
	}
	public int getDetails_price() {
		return details_price;
	}
	public void setDetails_price(int details_price) {
		this.details_price = details_price;
	}
	public int getDetails_num() {
		return details_num;
	}
	public void setDetails_num(int details_num) {
		this.details_num = details_num;
	}
	
}
