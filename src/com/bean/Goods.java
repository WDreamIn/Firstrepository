package com.bean;

public class Goods {
	private int goods_id;
	private String goods_name;
	private int goods_price;
	private  int goods_num;
	private String goods_img;
	private String goods_message;
	private String goods_author;
	public int getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(int goods_id) {
		this.goods_id = goods_id;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public int getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(int goods_price) {
		this.goods_price = goods_price;
	}
	public int getGoods_num() {
		return goods_num;
	}
	public void setGoods_num(int goods_num) {
		this.goods_num = goods_num;
	}
	public String getGoods_img() {
		return goods_img;
	}
	public void setGoods_img(String goods_img) {
		this.goods_img = goods_img;
	}
	public String getGoods_message() {
		return goods_message;
	}
	public void setGoods_message(String goods_message) {
		this.goods_message = goods_message;
	}
	public String getGoods_author() {
		return goods_author;
	}
	public void setGoods_author(String goods_author) {
		this.goods_author = goods_author;
	}
	
}
